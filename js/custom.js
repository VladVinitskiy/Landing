function menuClick() {
   document.querySelector('.menuBox').style.display='block';
   document.querySelector('.menuBox').style.zIndex=1;
}

function menuClose() {
    document.querySelector('.menuBox').style.display='none';
}

function hireUs() {
    document.querySelector('.hireUs').style.display='flex';
    document.querySelector('.hireUs').style.zIndex=1;
}

function hireUsClose() {
    document.querySelector('.hireUs').style.display='none';
}
